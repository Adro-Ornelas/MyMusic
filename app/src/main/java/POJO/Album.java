package POJO;

public class Album {
    int ID_album;
    int ID_discografia;
    String tituloAlbum;
    String fechaAlbum;
    String portadaAlbum;

    public int getID_album() {
        return ID_album;
    }

    public void setID_album(int ID_album) {
        this.ID_album = ID_album;
    }

    public int getID_discografia() {
        return ID_discografia;
    }

    public void setID_discografia(int ID_discografia) {
        this.ID_discografia = ID_discografia;
    }

    public String getTituloAlbum() {
        return tituloAlbum;
    }

    public void setTituloAlbum(String tituloAlbum) {
        this.tituloAlbum = tituloAlbum;
    }

    public String getFechaAlbum() {
        return fechaAlbum;
    }

    public void setFechaAlbum(String fechaAlbum) {
        this.fechaAlbum = fechaAlbum;
    }

    public String getPortadaAlbum() {
        return portadaAlbum;
    }

    public void setPortadaAlbum(String portadaAlbum) {
        this.portadaAlbum = portadaAlbum;
    }

}
